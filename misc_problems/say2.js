function say(text) {
  console.log("==> " + text);
}

say("hello");       // => hello
say("hi");          // => hi
say("how are you"); // => how are you
say("I'm fine");    // => I'm fine

// LS quote, "Run this code to see the result. We've now added a ==> to the beginning of each line.
